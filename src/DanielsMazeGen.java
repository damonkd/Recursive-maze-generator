    private static void makeMazeRecursive(char[][]level, int startX, int startY, int endX, int endY)
    {
    	
    	//if (FirstIteration == true || FirstIteration == false  )
    	// drawLevel(level);
		// System.out.println("----------------------------------------------------\n\n");
    	
    	//ends recursion if maze less than 3 wide or high
    	if(endX - startX <= 3 || endY - startY <= 3)  
    	return;
    		
    	//creates random row checks and fixes if row is = or next to startY and EndY
    	int row = rand(startY, endY);
    	
    	if (row == startY)
    		row= row +2;
    	if (row == startY + 1)
			row++;
    	if (row == endY)
			row = row - 2;
    	if (row == endY-1)
			row--;
		
    	//creates random column checks and fixes if row is = or next to startX and EndX
    	int  column= rand(startX, endX);
		
    	if (column == startX)
    		column = column +2;
		
		if (column == startX +1)
    		column ++;
		if (column == endX)
    		column = column -2;
		
		if (column == endX-1)
    		column --;
		
    	//if(row < 5 || column <5)
    		//return;
    	
    	//draws row
    	if(endY - startY >= 3)
    	{
    		for (int x = startX; x <= endX; x++)
             level[row][x] = ICON_WALL;//ONE;
    	} 
    	
    	//draws column
    	if(endX - startX >= 3)
    	{
    	for (int y = startY; y <= endY  ; y++)
             level[y][column] = ICON_WALL;//two;
    	}
    	
    	//punch holes in walls
    	level[row][rand(startX, column)] = ICON_BLANK;
    	level[row][rand(column, endX)] = ICON_BLANK;
    	
    	level[rand(startY, row)][column] = ICON_BLANK;
    	level[rand(row, endY)][column] = ICON_BLANK;
    	
    	//recursively splits 4 quadrants
    	 makeMazeRecursive(level, startX, startY, column, row);	//quad a
    	 makeMazeRecursive(level, column, startY, endX, row); // quad b
    	 makeMazeRecursive(level, startX, row, column, endY); // quad c
    	 makeMazeRecursive(level, column, row, endX, endY); //quad d
    
    	
    	}
    
